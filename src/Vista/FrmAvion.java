    public void limpiar() {

        txtSerialAvion.setText("");
        txtCapacidad.setText("");
        JcbxCodigopiloto.setSelectedItem("");
        JcbxCodigoprimeraazafata.setSelectedItem("");
        JcbxCodigosegundaazafata.setSelectedItem("");

    }

    public void listar() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo = controladorAvion.listarElementos(listaAvion);
        jTableAvion.setModel(modelo);

    }

    private void listarcomboxpiloto() {
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        for (int i = 0; i < listaEmpleado.size(); i++) {
            if (listaEmpleado.get(i).getCargo().equals("Piloto")) {
                modelo.addElement(listaEmpleado.get(i).getCedula() + " " + listaEmpleado.get(i).getNombre());
            }
            JcbxCodigopiloto.setModel(modelo);
        }
    }

    private void listarcomboxazafata() {
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        for (int i = 0; i < listaEmpleado.size(); i++) {
            if (listaEmpleado.get(i).getCargo().equals("Azafata")) {
                modelo.addElement(listaEmpleado.get(i).getCedula() + " " + listaEmpleado.get(i).getNombre());
            }
            JcbxCodigoprimeraazafata.setModel(modelo);
            JcbxCodigosegundaazafata.setModel(modelo);

        }
    }

