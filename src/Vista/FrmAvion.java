    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        String serial = txtSerialAvion.getText();
        int capacidad = Integer.parseInt(txtCapacidad.getText());
        String codigoPiloto = JcbxCodigopiloto.getSelectedItem().toString();
        String codigoPrimeraAzafata = JcbxCodigoprimeraazafata.getSelectedItem().toString();
        String codigoSegundaAzafata = JcbxCodigosegundaazafata.getSelectedItem().toString();
        listaAvion = listaAvion = controladorAvion.registrarAvion(listaAvion, serial, capacidad, codigoPiloto, codigoPrimeraAzafata, codigoSegundaAzafata);
        String res = controladorAvion.guardarArchivo(listaAvion);
        listar();
        limpiar();

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        String serial = JOptionPane.showInputDialog("Ingrese el serial del avion que desea buscar");
        txtSerialAvion.setEnabled(false);
        ClsAvion avion = null;
        avion = controladorAvion.buscarAvion(listaAvion, serial);
        if (avion == null) {
            limpiar();
        } else {
            txtSerialAvion.setText(avion.getSerial());
            txtCapacidad.setText(avion.getCapacidad() + "");
            JcbxCodigopiloto.setSelectedItem(avion.getCodigoPiloto());
            JcbxCodigoprimeraazafata.setSelectedItem(avion.getCodigoPrimeraazafata());
            JcbxCodigosegundaazafata.setSelectedItem(avion.getCodigoSegundaazafata());

        }

    }//GEN-LAST:event_btnBuscarActionPerformed

